IF EXISTS (SELECT * 
			FROM sys.objects 
			WHERE object_id = OBJECT_ID(N'[ADMINISTRATOR]') 
				AND type in (N'U'))
BEGIN
	DROP TABLE [ADMINISTRATOR]
END
GO

